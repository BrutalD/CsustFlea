function changeBg(num) {
	var str = "needBgImg";
	var check;
	var thingid;
	for ( var i = 1; i <= 12; i++) {
		if (i == num) {
			check = str + i;
			thingid = document.getElementById(check);

			thingid.className = "getBackgroundImg";
		} else {
			check = str + i;
			thingid = document.getElementById(check);

			thingid.className = "noBackgroundImg";
		}
	}
}