function change(obj, val) {
	obj.className = "selected";
	var need1 = document.getElementById("c1");
	var need2 = document.getElementById("c2");
	var need3 = document.getElementById("c3");
	if (val == "a") {
		need2.className = "noselected";
		need3.className = "noselected";
	}
	if (val == "b") {
		need1.className = "noselected";
		need3.className = "noselected";
	}
	if (val == "c") {
		need1.className = "noselected";
		need2.className = "noselected";
	}
}